class Employee:

    def __init__(self, name, id, dep, job):
        self.__name = name
        self.__id = id
        self.__dep = dep
        self.__job = job

# ****************************************************************************************************

    def set_name(self, name):
        self.__name = name

# ****************************************************************************************************

    def get_name(self):
        return self.__name

# ****************************************************************************************************

    def set_id(self, id):
        self.__id = id

# ****************************************************************************************************

    def get_id(self):
        return self.__id

# ****************************************************************************************************

    def set_dep(self, dep):
        self.__dep = dep

# ****************************************************************************************************

    def get_dep(self):
        return self.__dep

# ****************************************************************************************************

    def set_job(self, job):
        self.__job = job

# ****************************************************************************************************

    def get_job(self):
        return self.__job

# ****************************************************************************************************

    def __str__(self):
        return 'Name: ' + self.__name + '\nID Number: ' + self.__id + '\nDepartment: ' + self.__dep + '\nTitle: ' + self.__job